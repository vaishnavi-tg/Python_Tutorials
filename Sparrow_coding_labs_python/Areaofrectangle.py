#Write a Python program that takes the length and width of a rectangle as 
#input from the user and calculates its area. Display the result as "The area
# of the rectangle is X square units," where X is the calculated area.

len=int(input("Enter the length\n"))
wid=int(input("Enter the width\n"))
area=len*wid
print("The area of the rectangle is ", area,"square units")